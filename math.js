var num1 = 100;
var num2 = 15;
var add = num1 + num2; // Addition
var sub = num1 - num2; // Substitute
var mul = num1 * num2; // Multiplication
var div = num1 / num2; // Division
var rem = num1 % num2; // Remainder

// Method - 1 
// Recommended Method For Working With Multiple Variable
console.log(`Given numbers are : ${num1} and ${num2}`);
console.log(`Addition : ${add}`);
console.log(`Substitute : ${sub}`);
console.log(`Multiplication : ${mul}`);
console.log(`Division : ${div}`);
console.log(`Remainder : ${rem}`);

// Method - 2
console.log("Given numbers are : " + num1, num2);
console.log("Addition : " + add);
console.log("Substitute : " + sub);
console.log("Multiplication : " + mul);
console.log("Divison : " + div);
console.log("Remainder : " + rem);
